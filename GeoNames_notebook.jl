# ╔═╡ 294a335d-2f31-415a-b4b1-8577ba892b98
function get_api_response(params::String, url::String = URL)
	
	OM_request = HTTP.request("GET", 
		                       url * params; 
							   verbose = 0, 
							   retries = 2)
	
	response_text = String(OM_request.body)
	response_dict = JSON.parse(response_text)
	
	return response_dict
end

# ╔═╡ 23efaf62-41ec-4571-a704-a664776ce184
function fetch_lat_long(city::String)
	
	df_city = filter(row -> row.CITY == city, DF_CITIES)
	lat, long = 0, 0

	if isempty(df_city)
		error("Coordinates for city not found!")
	else			
		lat  = df_city[!, :LATITUDE][1]
		long = df_city[!, :LONGITUDE][1]
		timezone = df_city[!, :TIMEZONE][1]
	end

	return GeogCoord(lat, long, timezone)
	
end

